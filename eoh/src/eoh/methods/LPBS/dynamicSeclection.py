import gurobipy as gp
from gurobipy import GRB
import numpy as np

def dynamic_selection(population, i: int, n_pop=9):
    """
    动态选择个体
    参数：种群，个体的列表；当前代数；最大代数
    输出：被选择父母在种群中的索引
    """

    # ===== 1. 参数初始化 =====
    # 案例适应度 f = pop_size * case_size
    f = np.array([ind['case_fitness'] for ind in population], dtype=float)
    # 获取个体与案例数量
    pop_size, case_size = f.shape
    # 计算各案例最佳适应度
    f_max_c = f.max(axis=0)
    # 计算每一个个体的总适应度
    f_sum_i = f.sum(axis=1)
    # 计算f_sum的均值
    f_mean = np.mean(f_sum_i)
    # 计算最高个体的总适应度
    f_max = f_sum_i.max()
    # 计算G3
    G3 = f_mean * (n_pop - i - 1)/n_pop + f_max * (i - 1)/n_pop
    # 计算f_sum与均值的偏差
    # f_dev_i = np.maximum(0, f_mean - f_sum_i)
    f_dev_i = np.maximum(0, G3 - f_sum_i)

    # 创建模型
    model = gp.Model("Dynamic Selection")
    model.Params.OutputFlag = 0

    # ===== 2. 设置变量 =====
    # x[i] 是否选择个体 i
    x = model.addVars(pop_size, vtype=GRB.BINARY, name="x")
    # y[i, c] 1, 被选择中个体 i 在案例 c 上表现最佳
    y = model.addVars(pop_size, case_size, vtype=GRB.BINARY, name="y")
    # z[c] 被选中个体中在案例 c 的最高适应度
    z = model.addVars(case_size, vtype=GRB.CONTINUOUS, lb=-GRB.INFINITY, name="z")
    # 第一个目标：最高分的偏差
    d1 = model.addVar(lb=0.0, name="d1")
    # 第二个目标：父母数量偏差
    d2 = model.addVar(lb=0.0, name="d2")
    # 第三个目标：个体总分偏差
    d3 = model.addVar(lb=0.0, name="d3")

    # ===== 3. 设置约束 =====
    # 0. 至少选择2个个体
    model.addConstr(gp.quicksum(x[i] for i in range(pop_size)) >= 2)

    # 1. 每个案例的 z[c] 等于所选个体的最高得分
    # 1.1 每一个案例只能有1个y[i, c]
    for c in range(case_size):
        model.addConstr(gp.quicksum(y[i, c] for i in range(pop_size)) == 1)
    # 1.2 排除未被选中的个体
    for i in range(pop_size):
        for c in range(case_size):
            model.addConstr(y[i, c] <= x[i])
    # 1.3 计算z
    for c in range(case_size):
        model.addConstr(z[c] == gp.quicksum(y[i, c] * f[i, c] for i in range(pop_size)))

    # 2. 计算第一个目标
    model.addConstr(d1 == gp.quicksum(f_max_c[c] - z[c] for c in range(case_size)) , name=f"d1")

    # 3. 计算第二个目标
    G2 = min(pop_size * 0.5, 10)
    model.addConstr(d2 >= gp.quicksum(x[i] for i in range(pop_size)) - G2, name="d2")

    # 4. 计算第三个目标
    model.addConstr(d3 == gp.quicksum(x[i] * f_dev_i[i] for i in range(pop_size)), name="d3")

    # ===== 4. 目标函数 =====
    w1, w2, w3 = 0.5, 1.0, 0.1
    model.setObjective(w1 * d1 + w2 * d2 + w3 * d3, GRB.MINIMIZE)

    model.optimize()

    if model.Status == GRB.OPTIMAL:
        selected_indices = [i for i in range(pop_size) if x[i].X > 0.5]
        #print("\n选中的父母个体：")
        #for idx in selected_indices:
        #    ind = population[idx]
        #    print(f"个体 {idx}: 目标值: {ind['objective']}")
        #    print(f"案例适应度: {ind['case_fitness']}")
        #    print("-" * 40)
        return selected_indices
    else:
        print("未找到最优解，模型状态：", model.Status)
        return []

